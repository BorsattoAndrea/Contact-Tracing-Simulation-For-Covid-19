class Person:
    def __init__(self, id, house_id, first_name, last_name, gender, cell, city, email, dob, vaccinated):
        self.id = id
        self.house_id = house_id
        self.first_name = first_name
        self.last_name = last_name
        self.gender = gender
        self.cell = cell
        self.city = city
        self.email = email
        self.dob = dob
        self.vaccinated = vaccinated